        <h2 class="mt-4">1. Cracked Screen Repair</h2>
        <p><strong>DIY Tip:</strong> If you find yourself with a cracked screen, you can attempt a DIY repair with a screen replacement kit. Here's a general guide:</p>
        <ol>
            <li>Power Off the Device: Ensure your device is turned off before starting the repair.</li>
            <li>Remove the Back Cover: Use plastic opening tools to gently remove the back cover.</li>
            <li>Disconnect Components: Carefully disconnect any necessary components to access the screen.</li>
            <li>Remove the Cracked Screen: Use plastic opening tools to detach the broken screen.</li>
            <li>Install the Replacement Screen: Align the new screen and secure it in place, following the reverse steps.</li>
            <li>Test for Functionality: Power on the device and ensure the new screen functions properly.</li>
        </ol>

