                    <h2>Battery Replacement:</h2>
                    <ol>
                        <li>Power off the device and remove the back cover.</li>
                        <li>Locate and carefully disconnect the old battery.</li>
                        <li>Insert the new battery and secure it in place.</li>
                        <li>Reassemble the device and power it on to ensure the battery functions properly.</li>
                    </ol>

                    <h2>Charging Port Repair:</h2>
                    <ol>
                        <li>Disconnect the device and remove the back cover.</li>
                        <li>Locate the charging port and inspect for any damage.</li>
                        <li>If the port is damaged, desolder and replace it with a new one.</li>
                        <li>Reassemble the device and test the charging functionality.</li>
                    </ol>

                    <h2>Camera Issues:</h2>
                    <ol>
                        <li>Access the device's internal components.</li>
                        <li>Locate the camera module and disconnect it.</li>
                        <li>Replace the faulty camera module with a new one.</li>
                        <li>Test the camera to ensure proper functionality.</li>
                    </ol>

                    <h2>Water Damage Recovery:</h2>
                    <ol>
                        <li>Power off the device immediately and do not attempt to charge it.</li>
                        <li>Disassemble the device and carefully clean the components with isopropyl alcohol.</li>
                        <li>Allow the components to dry thoroughly before reassembly.</li>
                        <li>Power on the device and check for functionality.</li>
                    </ol>

